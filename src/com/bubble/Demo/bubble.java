package com.bubble.Demo;

public class bubble {
    public static void main(String[] args) {
        int num[] = {2, 6, 7, 16};


        System.out.println("before.sorting");
        for (int number : num) {
            System.out.print(number + " ");
        }



        System.out.println("after.sorting");
        for (int number : num) {
            System.out.print(number + " ");


        }
    }
}
